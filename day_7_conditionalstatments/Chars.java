package day_7_conditionalstatments;

public class Chars {

	public static void main(String[] args) {
		char letter ='a';//97
		letter ++;//98 b
		 System.out.println(letter);
		 int ascii = letter;
		 System.out.println(ascii);
		char myChar = 'B';
		
		--myChar;
		System.out.println(myChar);
		
		char symbol ='(';
		symbol ++;
		System.out.println(symbol);
		
		char ch =100;
		System.out.println(ch);
	}

}
