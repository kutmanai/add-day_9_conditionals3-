package mondaygroupproject_4;

public class LoopsPartTask1and2 {

	public static void main(String[] args) {
	 //1 task	 Write for and while loop so that print numbers between 1 and20
		int i=1;
		 while (i <=20) {
			 System.out.print( i + " " );
			 i++;
		 }
		 System.out.println();
		 
		  
		  for (int j=1; j<=20; j ++) {
			  System.out.print(j + " " );
		  }
		  System.out.println();
		  
		  //2 task Write for and while loops so that they print only the even number between 1 and 20
		  
		  int k=2;
		  while(k<=20) {
			  System.out.print(k + " ");
			  k=k +2;
		  }
		  System.out.println();
		  
		  for(int l=2; l<=20; l=l +2) {
			  System.out.print(l + " " );
		  }
		  
     
	}

}
