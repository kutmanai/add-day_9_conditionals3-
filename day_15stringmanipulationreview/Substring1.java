package day_15stringmanipulationreview;

import java.util.Scanner;

public class Substring1 {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		System.out.println("Please enter a word");
		String xcode=scan.nextLine();
		System.out.println(xcode.substring(1,5));
		System.out.println("Please enter another word");
		String xxcode=scan.nextLine();
		System.out.println(xxcode.substring(3,6));
		System.out.println("Please enter third word");
		String oxidex=scan.nextLine();
		System.out.println(oxidex.substring(02,6));
		
		//System.out.println("Plese enter another word");
		
	}

}
